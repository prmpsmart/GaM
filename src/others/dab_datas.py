def d(**kwargs):
    ret = {}
    for k, v in kwargs.items():
        if k == 'c': name = 'current'
        elif k == 'l': name = 'last'
        elif k == 'u': name = 'upfrontLoan'
        elif k == 'r': name = 'upfrontRepay'
        elif k == 'w': name = 'withdraw'
        elif k == 't': name = 'transfer'
        elif k == 'b': name = 'bto'
        elif k == 'n': name = 'next'
        elif k == 'p': name = 'paidout'
        else: name = k
        ret[name] = v
    return ret

